package au.org.intersect.exsite9.domain;

/**
 * The field to sort Groups on.
 */
public enum ResearchFileSortField
{
    /**
     * Sorts on the file name.
     */
    NAME,

    /**
     * Sorts on the file size.
     */
    SIZE,

    /**
     * Sorts on the file extension.
     */
    EXTENSION,

    /**
     * Sorts on the modified date.
     */
    MODIFIED_DATE;
}
